# 定义有默认参数的函数

##你想定义一个函数或者方法，它的一个或多个参数是可选的并且有一个默认值。

def spam(a, b=42):
    print(a, b)

spam(1) # Ok. a=1, b=42
spam(1, 2) # Ok. a=1, b=2